class A:
    pass

# Каждый класс в Python сам по себе объект (экземпляр метакласса type), 
# поэтому для него можно получить идентификатор через встроенную функцию id():
print(id(A))  # 94662470010560

# id(A) возвращает уникальный для данного класса числовой идентификатор, 
# который обычно совпадает с адресом объекта в памяти (реализация-зависимо).

# Также у класса есть имя, доступное через A.__name__, 
# и его можно использовать как логический “идентификатор” в коде:
print(A.__name__)  # 'A'

# Если нужно отличать классы не по имени, 
# а именно как объекты, то корректно сравнивать их по идентичности (is) или по id().
